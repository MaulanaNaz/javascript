//Luas Segitiga
// L = 1/2*alas*tinggi

let alas = 5;
let tinggi = 9;

const Segitiga = 1 / 2 * alas * tinggi;
console.log(Segitiga)

//Luas Persegi Panjang
// L = a*b

let sisia = 6;
let sisib = 10;

const persegiPanjang = sisia * sisib;
console.log(persegiPanjang)

//Luas Lingkaran
// L = π*r*r
// π = 22/7  atau  3.14

r = 10;
π = 3.14;

const lingkaran = π * r * r;
console.log(lingkaran)

//Luas Belah Ketupat
// L = 1/2*d1*d2

d1 = 8;
d2 = 12;

const belahKetupat = 1 / 2 * d1 * d2;
console.log(belahKetupat)

//Luas Trapesium
// L = 1/2*a+b*tinggi

